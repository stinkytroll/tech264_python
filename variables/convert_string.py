int_string = "6"

# convert int_string to an integer
int_value = int(int_string)

# after converting, print its data type to the screen
print(int_value, type(int_value))  # Print 6 and class type int

# convert int_string to float
int_float = float(int_value)

# after converting, print its data type to the screen
print(int_float, type(int_float))  # Print 6.0 and class type float

