# Slicing Strings
# You can return a range of characters by using the slice syntax.
# Specify the start index and the end index, separated by a :, to return a part of the string.

Hw = "Hello world!"
print(Hw[2:6])  # The 2 is the start, the 6 is the end.

print(Hw)

# Find out how many characters Hw has
print(len(Hw))

# Get the character in the first position in Hw
print(Hw[0])

# Get the last character
print(Hw[-1])

# Get the 2nd last character
print(Hw[-2])

# Print starting from the 2nd character
print(Hw[2:])

# Print the last 3 characters of the string
print(Hw[-3:])

# Print the first 5 characters
print(Hw[:5])

# Print characters starting 1 and ending before 5
print(Hw[1:5])

